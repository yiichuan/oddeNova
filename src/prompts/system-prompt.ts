export function buildUserMessage(instruction: string, currentCode: string): string {
  if (currentCode) {
    return `Current playing code:\n\`\`\`\n${currentCode}\n\`\`\`\n\nUser instruction: ${instruction}`;
  }
  return `User instruction: ${instruction}`;
}
